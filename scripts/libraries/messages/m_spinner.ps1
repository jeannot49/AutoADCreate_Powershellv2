function DisplaySpinner {
    $spinner = "|", "/", "-", "\";
    $padding = (" " * ($host.UI.RawUI.MinWindowSize.Width + 1)) -replace "`n", ""
    
    While (1) {
        ForEach ($i in $spinner) {
            Write-Host -NoNewLine -ForegroundColor Cyan "`r $padding $i"
            Start-Sleep -Milliseconds 250
        }
    }    
}